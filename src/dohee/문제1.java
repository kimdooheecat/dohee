public class 문제1 {

	public static void main(String[] args) 
	{
		for(int i = 0; i <10 ; i++)   //i= 반복할 갯수 
		{
			for(int j=0; j<=i; j++)  // 0도 허용이 되어야 하기 때문에 j=i도 같아야 한다.
			{
				System.out.print("O"); 
			}
			System.out.println( ""); // 첫 번째가 출력 되고 난 뒤에 줄바꿈이 일어나야함.
			  
			
		}
	}
 
}








